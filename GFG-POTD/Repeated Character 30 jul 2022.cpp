class Solution
{
  public:
    char firstRep (string s)
    {
        vector<int> count(26,0);
        for(char x: s) count[x-97]++;
        for(char x: s) if(count[x-97] > 1) return x;
        return '#';
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string s; cin >> s;
        Solution ob;
        char res = ob.firstRep (s);
        if (res == '#') cout << -1 << endl;
        else cout << res << endl;
    }
    return 0;
}



